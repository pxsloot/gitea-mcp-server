class TestFormatDateTime:
    """Tests for _format_datetime."""

    def test_formats_iso_datetime(self):
        """Test ISO datetime string is formatted correctly."""
        dt = "2024-01-15T10:30:00Z"
        result = _format_datetime(dt)
        assert result == "2024-01-15 10:30:00 UTC"

    def test_handles_none(self):
        """Test None returns N/A."""
        assert _format_datetime(None) == "N/A"

    def test_handles_empty_string(self):
        """Test empty string returns N/A."""
        assert _format_datetime("") == "N/A"

    def test_handles_invalid_format(self):
        """Test invalid format returns original string."""
        assert _format_datetime("not a date") == "not a date"


class TestFormatRepoMarkdown:
    """Tests for _format_repo_markdown."""

    def test_formats_repo_completely(self):
        """Test repository is formatted with all fields."""
        repo = {
            "full_name": "owner/repo",
            "description": "Test repo",
            "owner": {"login": "owner"},
            "html_url": "https://example.com/owner/repo",
            "default_branch": "main",
            "stargazers_count": 42,
            "forks_count": 10,
            "open_issues_count": 5,
            "size": 1024,
            "created_at": "2024-01-01T00:00:00Z",
            "updated_at": "2024-01-15T00:00:00Z",
            "topics": ["test", "example"],
            "license": {"name": "MIT"},
        }
        result = _format_repo_markdown(repo)

        assert "# owner/repo" in result
        assert "**Description**: Test repo" in result
        assert "| Owner | owner |" in result
        assert "| Stars | 42 |" in result
        assert "**Topics**: test, example" in result
        assert "**License**: MIT" in result

    def test_handles_missing_fields(self):
        """Test repo with missing optional fields."""
        repo = {
            "full_name": "owner/repo",
            "owner": {"login": "owner"},
            "html_url": "https://example.com/owner/repo",
        }
        result = _format_repo_markdown(repo)

        assert "# owner/repo" in result
        assert "No description" in result
        assert "None" in result  # topics, license etc.


class TestRegisterAutoGeneratedResources:
    """Tests for register_auto_generated_resources."""

    @pytest.fixture
    def mock_mcp(self):
        """Create a mock FastMCP instance."""
        mcp = MagicMock(spec=FastMCP)
        # Mock the resource decorator to track registrations
        mcp.resource = MagicMock(return_value=lambda func: func)
        return mcp

    @pytest.fixture
    def mock_gitea_client(self):
        """Create a mock GiteaClient."""
        client = AsyncMock()
        return client

    @pytest.fixture
    def openapi_spec_with_get(self):
        """OpenAPI spec with a simple GET endpoint."""
        return {
            "paths": {
                "/repos/{owner}/{repo}": {
                    "get": {
                        "summary": "Get repository information",
                        "description": "Get details about a repository",
                        "operationId": "getRepo",
                        "parameters": [
                            {
                                "name": "owner",
                                "in": "path",
                                "required": True,
                                "schema": {"type": "string"},
                            },
                            {
                                "name": "repo",
                                "in": "path",
                                "required": True,
                                "schema": {"type": "string"},
                            },
                        ],
                    }
                },
                "/repos/{owner}/{repo}/issues": {
                    "get": {
                        "summary": "List repository issues",
                        "operationId": "listRepoIssues",
                        "parameters": [
                            {
                                "name": "owner",
                                "in": "path",
                                "required": True,
                                "schema": {"type": "string"},
                            },
                            {
                                "name": "repo",
                                "in": "path",
                                "required": True,
                                "schema": {"type": "string"},
                            },
                            {
                                "name": "state",
                                "in": "query",
                                "required": False,
                                "schema": {"type": "string"},
                            },
                        ],
                    }
                },
            }
        }

    async def test_registers_resources_for_get_endpoints(
        self, mock_mcp, mock_gitea_client, openapi_spec_with_get
    ):
        """Test that GET endpoints are registered as resources."""
        register_auto_generated_resources(
            mock_mcp, mock_gitea_client, openapi_spec_with_get, skip_uris=set()
        )

        # Should have called resource decorator twice (2 GET endpoints)
        assert mock_mcp.resource.call_count == 2

        # Check URI templates
        uri_templates = [call[0][0] for call in mock_mcp.resource.call_args_list]
        assert "gitea://repos/{owner}/{repo}" in uri_templates
        assert "gitea://repos/{owner}/{repo}/issues" in uri_templates

    async def test_resource_function_has_docstring(
        self, mock_mcp, mock_gitea_client, openapi_spec_with_get
    ):
        """Test that auto-generated resources have proper docstrings."""
        # Track registered functions
        registered = []

        def track_decorator(uri, **kwargs):
            def decorator(func):
                registered.append((uri, func))
                return func

            return decorator

        mock_mcp.resource = MagicMock(side_effect=track_decorator)

        register_auto_generated_resources(
            mock_mcp, mock_gitea_client, openapi_spec_with_get, skip_uris=set()
        )

        # Find the resource for /repos/{owner}/{repo}
        repo_entry = next(r for r in registered if r[0] == "gitea://repos/{owner}/{repo}")
        resource_func = repo_entry[1]

        assert resource_func.__doc__ is not None
        assert "Get repository information" in resource_func.__doc__

    async def test_ignores_non_get_methods(self, mock_mcp, mock_gitea_client):
        """Test that POST/PUT/DELETE endpoints are not registered."""
        spec = {
            "paths": {
                "/repos/{owner}/{repo}": {
                    "post": {"summary": "Create repo", "operationId": "createRepo"},
                    "put": {"summary": "Update repo", "operationId": "updateRepo"},
                }
            }
        }
        register_auto_generated_resources(mock_mcp, mock_gitea_client, spec)

        # Should not register any resources (no GET)
        mock_mcp.resource.assert_not_called()

    async def test_handles_empty_spec(self, mock_mcp, mock_gitea_client):
        """Test that empty spec doesn't crash."""
        spec = {"paths": {}}
        register_auto_generated_resources(mock_mcp, mock_gitea_client, spec)
        mock_mcp.resource.assert_not_called()


class TestRegisterCustomResources:
    """Tests for register_custom_resources."""

    @pytest.fixture
    def mock_mcp(self):
        """Create a mock FastMCP instance."""
        mcp = MagicMock(spec=FastMCP)
        return mcp

    @pytest.fixture
    def mock_gitea_client(self):
        """Create a mock GiteaClient."""
        client = AsyncMock()
        return client

    async def test_registers_all_custom_resources(self, mock_mcp, mock_gitea_client):
        """Test that all expected custom resources are registered."""
        register_custom_resources(mock_mcp, mock_gitea_client)

        # Should register 11 custom resources
        assert mock_mcp.resource.call_count == 11

        uri_templates = [call[0][0] for call in mock_mcp.resource.call_args_list]

        expected = [
            "gitea://repos/{owner}/{repo}",
            "gitea://repos/{owner}/{repo}/readme",
            "gitea://repos/{owner}/{repo}/issues",
            "gitea://repos/{owner}/{repo}/issues/open",
            "gitea://repos/{owner}/{repo}/issues/closed",
            "gitea://repos/{owner}/{repo}/pulls",
            "gitea://repos/{owner}/{repo}/pulls/open",
            "gitea://repos/{owner}/{repo}/files/{path}",
            "gitea://repos/{owner}/{repo}/releases",
            "gitea://users/{username}",
            "gitea://orgs/{orgname}",
        ]
        for template in expected:
            assert template in uri_templates


